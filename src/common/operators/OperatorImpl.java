package common.operators;

public class OperatorImpl
{
    public static void main(String[] args) {
        //Unary Operator
        int a = 50;
        System.out.println("a++:"+ a++); //50
        System.out.println("++a"+ ++a);  //52
        int b= 10;
        System.out.println("b--"+ b--); //10
        System.out.println("--b"+ --b); //8

    }
}
